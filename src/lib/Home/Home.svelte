          改善經濟狀況的第二步是發展“輕工業”。
          政府開始幫助建立小工廠，生產紡織品，鞋和紙。
          這幫助台灣準備轉移到“重工業”。
          為了刺激投資，政府鼓勵世界各地的投資者在台灣港口建立工廠。
